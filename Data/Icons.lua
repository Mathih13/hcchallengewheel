function Icons:GetIconData()
    self:LoadData()

    local allTalents = {} 
    for class, talents in pairs(self.data) do
        for _, talent in ipairs(talents) do
            table.insert(allTalents, talent)
        end
    end

    return allTalents 
end

-- Function to release the data and free memory
function Icons:Release()
    self.data = nil -- Remove the reference to the data
    collectgarbage("collect") -- Run garbage collection to free memory
end